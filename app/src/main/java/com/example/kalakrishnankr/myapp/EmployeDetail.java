public class EmployeDetail {
    public String name;
    public String lname;
    public String dob;
    public String doj;
    public String department;
    public String position;
    public String salary;

    public EmployeDetail(String name,String lname,String dob,String doj,String department,String position,String salary){
        this.name=name;
        this.lname=lname;
        this.dob=dob;
        this.doj=doj;
        this.department=department;
        this.position=position;
        this.salary=salary;
    }
}


